    private void cek_kode(){
        try {
            db.stmt.executeQuery("select*from buku where"+"Kode_Buku="+jTextKodeBuku.getText()+"");
            if(db.rs.next()){   
                if(jTextKodeBuku.getText().equals(db.rs.getString("Kode_Buku"))){
                    JOptionPane.showMessageDialog(null, "Sudah tersedia buku dengan kode : "+jTextKodeBuku.getText());
                }
            }else{
                JOptionPane.showMessageDialog(null, "Buku berhasil dimasukan");
            }
        }catch (SQLException es){
            es.printStackTrace();
        }
    }
    private void refreshTable(){
        try {
            BukuModel book = bukuC.getLastBook();
            tableModel.addRow(new String[]{book.getKode(), book.getJudul(), book.getPengarang(), book.getPenerbit(),
            book.getTanggalTerbit().toString(), book.getIsbn(), book.getEdisi(), book.getKolasi(), book.getJumlah(),
            book.getHarga(), book.getHarga()});
            tableModel.fireTableDataChanged();
            notif(tableModel);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private synchronized void notif(TableModel tableModel){
        tableModel.notifyAll();
    }
    
    private void refreshTableDDC(){
        try{
            DDCModel ddc = ddcC.getLastDDC();
            tableModelD.addRow(new String[]{ddc.getNomer_ddc(), ddc.getNama_ddc(), ddc.getDeskripsi()});
            tableModelD.fireTableDataChanged();
            notif(tableModel);
        }catch(SQLException ey){
            ey.printStackTrace();
        }
    }
    
