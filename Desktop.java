    private void fillBookTable(){
        try{
            ArrayList<BukuModel> books = bukuC.retrieveBooks();
            if(books.size() == 0)
                System.out.println("Buku Kosong");
            
            if(tableModel.getRowCount() > 0){
                for(int i = 0; i < tableModel.getRowCount(); i++)
                    tableModel.removeRow(i);
                tableModel.removeRow(0);
            }
            
            for(BukuModel b : books){
                tableModel.addRow(new String[]{b.getKode(), b.getJudul(), b.getPengarang(), b.getPenerbit(), b.getTanggalTerbit().toString(),
                b.getIsbn(), b.getEdisi(), b.getKolasi(), b.getJumlah(), b.getJumlah(), b.getHarga(), b.getDeskripsi()});
            }
            tableModel.fireTableDataChanged();
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
